package day0620;

public class SubString_15 {

	public static void main(String[] args) {
		// 문장에서 문자열을 뽑는것
		
		String str="나는 쌍용에서 자바를 공부중입니다";
		
		char ch=str.charAt(3);
		String word1=str.substring(8);
		String word2=str.substring(8, 11);//추출하려는 숫자에 마지막값은 +1을 해줘야 원하는 문자들이 나온다
		int i=str.length();//총 길이, 갯수(공백포함), 출력하는 형태가 int형이므로 int 변수
		
		System.out.println(ch);
		System.out.println(word1);
		System.out.println(word2);
		System.out.println(i);

	}

}
